package de.nilsreiter.web.beans.menu;

public abstract class AbstractMenuEntry {
	int level;

	String name;

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
